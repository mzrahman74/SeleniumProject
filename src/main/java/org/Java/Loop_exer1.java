package org.Java;

public class Loop_exer1 {
    public static void main(String[] args) {

        for (int i=1; i<5; i++){
            // System.out.println(i);
            for (int j=1; j<=i; j++){
                System.out.print(j);
                System.out.print("\t");


            }
            System.out.println("");
        }


    }
}

